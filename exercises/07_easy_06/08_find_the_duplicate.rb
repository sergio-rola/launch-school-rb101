def find_dup(arr)
  incremental_arr = [arr.first]
  idx = 1
  duplicate = nil

  while idx < arr.size
    if incremental_arr.include?(arr[idx])
      duplicate = arr[idx]
    end
    break if incremental_arr.include?(arr[idx])

    incremental_arr << arr[idx]

    idx += 1
  end

   duplicate
end

